import math

print(round(1.3))  # retorna redondeado al mas cercano
# retorna el valor absoluto, si es positivo o negativo (77 en este caso)
print(abs(-77))
print(math.ceil(1.1))  # redondea hacia arriba
print(math.floor(1.1))  # redondea hacia abajo

# retorna un boolean diciendo si un numero es un numero o no
print(math.isnan(1.1))
print(math.pow(10, 3))  # 10 potenciado a la 3
print(math.sqrt(9))  # retorna la raiz cuadrada del valor que le paso
